from abc import ABC, abstractmethod
import asyncio
from numpy import array
from dataclasses import dataclass
from DataTypes import Attitude, Velocity, RelativePosition, \
    GlobalPosition, Magnetometer, Status

@dataclass
class Response:
    """
    Response generated by device commands.

    :param response: True indicates that the command was accepted
        and False indicates that the command was rejected.
    :type response: bool
    :param message: Message string to provide more information.
    :type message: str
    """
    response: bool
    message: str

    def __bool__(self):
        """
        Overloaded boolean operator to support easy success checks.
        """
        return self.response


@dataclass
class ImageFrame:
    """
    Holds data from imaging sensors.

    :param data: Sensor data
    :type data: class `numpy.array`
    :param image_sensor_id: Source image sensor
    :type image_sensor_id: int
    :param timestamp: Timestamp of the data
    :type timestamp: int
    """
    data: array
    image_sensor_id: int
    timestamp: int


@dataclass
class TelemetryFrame:
    """
    Holds telemetry data from the drone.

    :param name: Name of the drone device
    :type name: str
    :param attitude: Attitude of the drone
    :type attitude: class: `base.Attitude`, optional
    :param velocity: Velocity reading of the drone
    :type velocity: class: `base.Velocity`, optional
    :param relative_position: Relative position of the drone
    :type relative_position: class: `base.RelativePosition`, optional
    :param global_position: Global position of the drone
    :type global_position: class: `base.GlobalPosition`, optional
    :param magnetometer: Magnetometer reading from the drone
    :type magnetometer: class: `base.Magnetometer`, optional
    :param satellites: Number of satellites visible to the drone
    :type satellites: int, optional
    :param battery: Battery of the drone, in percentage
    :type battery: int, optional
    :param gimbal_attitude: Attitude of the drone gimbal
    :type gimbal_attitude: class: `base.Attitude`, optional
    :param active_image_sensors: Active streaming image sensors from the
        drone
    :type active_image_sensors: list, optional
    :param status: Current status of the drone
    :type status: class: `base.Status`, optional
    """
    name: str
    attitude: Attitude = Attitude()
    velocity: Velocity = Velocity()
    relative_position: RelativePosition = RelativePosition()
    global_position: GlobalPosition = GlobalPosition()
    magnetometer: Magnetometer = 0
    satellites: int = None
    battery: int = None
    gimbal_attitude: Attitude = None
    active_image_sensors: list = None
    status: Status = 0


class IDroneDevice(ABC):
    """
    The interface for Python drone devices. Defines the operations
    that device classes must implement for full compatibility.
    """
    
    @abstractmethod
    async def connect(self):
        """
        Connect to the drone hardware.

        :return: 'True' if successful, 'False' otherwise 
        :rtype: bool
        """
        pass

    @abstractmethod
    async def isConnected(self):
        """
        Checks to see if the drone hardware is connected.

        :return: 'True' if connected, 'False' otherwise 
        :rtype: bool
        """
        pass

    @abstractmethod
    async def disconnect(self):
        """
        Disconnects from the drone hardware.
        """
        pass

    @abstractmethod
    async def takeOff(self):
        """
        Instruct the drone to arm and take off.
        
        :return: Response object
        :rtype: class: `base.Response`
        """
        pass

    @abstractmethod
    async def land(self):
        """
        Instruct the drone to land and disarm.
        
        :return: Response object
        :rtype: class: `base.Response`
        """
        pass

    @abstractmethod
    async def setHome(self, global_position):
        """
        Set the home destination for the drone.
        
        :param global_position: New home position
        :type global_position: class: `base.GlobalPosition`
        :return: Response object  
        :rtype: class: `base.Response`
        """
        pass

    @abstractmethod
    async def rth(self):
        """
        Return to home destination.

        :return: Response object
        :rtype: class: `base.Response`
        """
        pass

    @abstractmethod
    async def setAttitude(self, attitude, thrust):
        """
        Set the attitude of the drone.

        :param attitude: Target attitude
        :type attitude: class: `base.Attitude`
        :param thrust: Target thrust percentage, normalized from 0 to 1
        :type thrust: float
        :return: Response object
        :rtype: class: `base.Response`
        """
        pass

    @abstractmethod
    async def setVeloctiy(self, velocity):
        """
        Set the velocity of the drone.

        :param velocity: Target velocity
        :type velocity: class: `base.Velocity`
        :return: Response object
        :rtype: class: `base.Response`
        """
        pass

    @abstractmethod
    async def setGPSLocation(self, global_position, bearing, use_rel_alt=False):
        """
        Set a target GPS location for the drone.

        :param global_position: Target global position
        :type global_position: class: `base.GlobalPosition`
        :param bearing: Target bearing, in degrees
        :type bearing: float
        :param use_rel_alt: Flag for using relative altitude; if True
            will consider the altitude parameter in global_position
            to be relative altitude above take off location
        :type use_rel_alt: bool, optional
        :return: Response object
        :rtype: class: `base.Response`
        """
        pass

    @abstractmethod
    async def setRelativePosition(self, relative_position, bearing):
        """
        Set a target position for the drone, relative to the take off
        position in meters.

        :param north: Target position along the north axis, in meters.
        :type north: float
        :param east: Target position along the east axis, in meters.
        :type east: float
        :param up: Target position along the up axis, in meters.
        :type up: float
        :param bearing: Target bearing, in degrees
        :type bearing: float
        :return: Response object
        :rtype: class: `base.Response`
        """
        pass

    @abstractmethod
    async def hover(self):
        """
        Instruct the drone to hover.

        :return: Response object
        :rtype: class: `base.Response`
        """
        pass

    @abstractmethod
    async def startStreaming(self, image_sensor_id):
        """
        Start streaming from the indicated imaging sensor.

        :param image_sensor_id: Target imaging sensor to stream from.
        :type image_sensor_id: int
        :return: Response object
        :rtype: class: Response
        :rtype: class: `base.Response`
        """
        pass

    @abstractmethod
    async def stopStreaming(self, image_sensor_id):
        """
        Stop streaming from the indicated imaging sensor.

        :param image_sensor_id: Target imaging sensor to stop streaming 
            from.
        :type image_sensor_id: int
        :return: Response object
        :rtype: class: `base.Response`
        """
        pass

    @abstractmethod
    async def getImageFrame(self, image_sensor_id):
        """
        Retrieve a frame from the indicated imaging sensor.

        :param image_sensor_id: Target imaging sensor to retrieve data 
            from.
        :type image_sensor_id: int
        :return: Image sensor frame, None if invalid image_sensor_id
        :rtype: class: `base.ImageFrame`
        """
        pass

    @abstractmethod
    async def getTelemetry(self):
        """
        Retrieve telemetry from the drone.

        :return: Telemetry frame
        :rtype: class: `base.TelemetryFrame`
        """
        pass
